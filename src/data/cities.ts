export function filterCities(query: string, limit = 8): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return RU_CITIES.slice(0, limit);
  return RU_CITIES.filter((c) => c.toLowerCase().includes(q)).slice(0, limit);
}

